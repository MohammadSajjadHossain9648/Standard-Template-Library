/*
    Memory allocation:
    Reserving or providing space to a variable is called memory allocation. For storing the data, memory allocation
    can be done in two ways -

    1.Static allocation or compile-time allocation - Static memory allocation means providing space for the variable.
    The size and data type of the variable is known, and it remains constant throughout the program. Static allocation
    happen in stack memory.
    2.Dynamic allocation or run-time allocation - The allocation in which memory is allocated dynamically. In this type
    of allocation, the exact size of the variable is not known in advance. Pointers play a major role in dynamic memory
    allocation. Dynamic allocation happen in heap memory.
*/
